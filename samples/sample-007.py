# Tuples
# Tuples are immutable sequences of objects
# Tuples are defined by enclosing the elements in parentheses
# Tuples can be used to store multiple values in a single variable
# Tuples can be used to return multiple values from a function
# Tuples can be used to unpack values into multiple variables

position = (1, 2, 3)
print('Position:', position)
print('Type of position:', type(position))
